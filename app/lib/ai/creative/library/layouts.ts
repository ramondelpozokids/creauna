export function getLayoutById(id: string): LayoutMeta | undefined {
  return LAYOUT_LIBRARY.find((layout) => layout.id === id);
}

export function listLayouts(): LayoutMeta[] {
  return [...LAYOUT_LIBRARY];
}

export function layoutsForSector(sector: string): LayoutMeta[] {
  const normalized = sector.trim().toLowerCase();
  return LAYOUT_LIBRARY.filter(
    (layout) =>
      layout.sectors.includes('*') ||
      layout.sectors.some((s) => s.toLowerCase() === normalized),
  );
}
